            <h6><b>Tentang Sistem Informasi Ruang Baca Fasilkom UNSRI</b></h6><hr>
            <p>
                Fasilkom Unsri merupakan fakultas yang memiliki kefokusan terhadap ilmu dibidang komputer. Visi dari Fasilkom Unsri yaitu untuk menjadi institusi yang unggul di bidang ICT, berintegritas dan berjiwa echnopreunership. Tujuan dari Fasilkom Unsri adalah berperan dalam menyelesaikan permasalahan sosial dan pembangunan melalui pengabdian kepada masyarakat yang berkelanjutan dalam bidang informasi, komunikasi dan teknologi.<p>
Sistem Ruang Baca Fasilkom Unsri dapat menghasilkan output sesuai dengan tujuan yang hendak dicapai oleh Fasilkom Unsri, maka dalam hal ini Fakultas perlu melibatkan mahasiswa. Selain itu, sistem ini dapat memberikan fasilitas yang mewadahi dalam proses belajar mahasiswa saat di kampus. Salah satunya dengan memaksimalkan Ruang Baca Fasilkom Unsri dan diharapkan dapat menjadi perpustakaan yang mandiri untuk Fasilkom. Mahasiswa Fasilkom Unsri sering datang ke ruang baca untuk mengulang atau memperdalam hal-hal yang telah dipelajari saat di kelas. Mahasiswa dapat meminjam buku untuk menambah referensi, sehingga banyak proses peminjaman di Ruang Baca Fasilkom Unsri. Hal ini menyebabkan banyaknya pencatatan data mahasiswa terhadap peminjaman dan pengembalian. Perlu diketahui bahwa peminjaman ini tidak hanya dilakukan oleh mahasiswa namun dosen juga banyak melakukan peminjaman buku ke pegawai atau Pegawai Ruang Baca Fasilkom.
<p>

Sistem saat ini melakukan pendataan untuk peminjaman dan pengembalian buku masih secara manual. Meskipun sudah melibatkan komputer, namun hasilnya belum maksimal karena datanya tidak terintegrasi dan pencatatan data ini terlalu banyak sehingga mengakibatkan informasi yang dihasilkan dari proses pengolahan data ini kurang efesien dan sering terjadinya duplikat data serta kesulitan dalam pencarian data sesuai kebutuhan.
<p>
Oleh karena itulah dibutuhkan suatu sistem informasi ruang baca yang terintegrasi. Sistem ini dapat diharapkan memberikan dukungan dalam mewujudkan suatu proses pengolahan data peminjaman dan pengembalian buku yang efektif dan juga efesien di Fasilkom Unsri.

