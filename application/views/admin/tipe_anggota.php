<script>
const nominalInput = document.getElementById('denda');

// Ketika pengguna mengetik di input
nominalInput.addEventListener('input', function () {
    // Dapatkan nilai input
    let denda = nominalInput.value;

    // Hapus semua tanda titik
    denda = denda.replace(/\./g, '');

    // Format dengan tanda titik
    denda = formatWithCommas(denda);

    // Masukkan kembali nilai ke input
    nominalInput.value = denda;
});

// Fungsi untuk memformat dengan tanda titik
function formatWithCommas(number) {
    // Gunakan Intl.NumberFormat untuk memformat dengan tanda titik
    return new Intl.NumberFormat('id-ID').format(number);
}

</script>

