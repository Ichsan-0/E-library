<script>
    $(document).ready(function () {
        function handleDelete(id, kala_terbit) {
            if (confirm("Apakah Anda yakin ingin menghapus data Tempat [" + kala_terbit + "] ini?")) {
                $.post("<?php echo base_url('admin/referensi/Kala_terbit/deleteTerbit/'); ?>" + id, function (response) {
                    if (response.status === "success") {
                        alert(response.message);
                        location.reload();
                    }
                }, 'json');
            }
        }

        // Add event listener to the "Hapus" buttons
        $(document).on('click', '.btnDeleteGmd', function () {
            var id = $(this).data("id");
			var kala_terbit = $(this).data("kala_terbit");
            handleDelete(id, kala_terbit);
        });

        // Additional event listener for the "Edit" buttons
        $(document).on('click', '.btnEditTipe', function () {
            var terbitId = $(this).data('id');

            // AJAX request to fetch GMD data
            $.ajax({
                url: 'Kala_terbit/editTerbit/' + terbitId,
                type: 'GET',
                dataType: 'json',
                success: function (data) {
                    $('#terbitId').val(data.id);
					$('#kala_terbit').val(data.kala_terbit);
					$('#id_bahasa').val(data.id_bahasa);
					$('#selang_waktu').val(data.selang_waktu);
					$('#satuan_waktu').val(data.satuan_waktu);
                    $('#editModal').modal('show');
                },
                error: function (xhr, status, error) {
                    console.log(error);
                }
            });
        });

        function showUpdateSuccessAlert(message) {
            $("#updateSuccessAlert strong").text(message);
            $("#updateSuccessAlert").show();
        }

        function hideUpdateSuccessAlert() {
            $("#updateSuccessAlert").hide();
        }

        function handleUpdate() {
            var terbitId    = $("#terbitId").val();
            var kala_terbit = $("#kala_terbit").val();
			var id_bahasa   = $("#id_bahasa").val();
			var selang_waktu = $("#selang_waktu").val();
			var satuan_waktu = $("#satuan_waktu").val();

            $.ajax({
                url: 'Kala_terbit/updateTerbit',
                type: "post",
                data: {
                    id: terbitId,
					kala_terbit : kala_terbit,
					id_bahasa : id_bahasa,
					selang_waktu : selang_waktu,
					satuan_waktu : satuan_waktu
                },
                dataType: "json",
                success: function (response) {
                    if (response.status === "success") {
                        showUpdateSuccessAlert(response.message);
                        $("#editTipeForm")[0].reset();
                        alert(response.message);
                        location.reload();
                    } else if (response.status === "error") {
                        alert(response.message); // Show error message if needed
                    }
                },
                error: function () {
                    alert("Terjadi kesalahan saat memproses permintaan.");
                }
            });
        }

        $("#btnUpdateTipe").click(function () {
            handleUpdate();
        });
    });
</script>
